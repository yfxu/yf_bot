# A bunch of common markdowns available in Discord

def bold(message):
	return "**{}**".format(message)

def italic(message):
	return "*{}*".format(message)

def bold_italic(message):
	return "***{}***".format(message)

def code(message):
	return "`{}`".format(message)

def code_block(message):
	return "```{}```".format(message)

def quote(message):
	return "> {}".format(message)

def quote_block(message):
	return ">>> {}".format(message)

def underline(message):
	return "**{}**".format(message)

def strikethrough(message):
	return "~~{}~~".format(message)
